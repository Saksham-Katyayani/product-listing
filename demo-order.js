const uploadOrdersInBatches = async (filePath) => {
    const failedOrders = [];
    
    try {
        // Read the file content (assuming it's a JSON file)
        const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

        if (!Array.isArray(data)) {
            throw new Error('Invalid data structure. Root element should be an array.');
        }

        const orders = data;

        for (let i = 0; i < orders.length; i += BATCH_SIZE) {
            const batch = orders.slice(i, i + BATCH_SIZE);

            console.log(`Processing batch ${Math.floor(i / BATCH_SIZE) + 1}...`);

            const results = await Promise.all(
                batch.map(async (order) => {
                    try {
                        const response = await axios.post(url, order, { headers });
                        if (response.status === 201) {
                            console.log(`Order ${order.order_id} uploaded successfully.`);
                        } else {
                            console.log(`Order ${order.order_id} failed: ${response.status}`);
                            failedOrders.push(order.order_id);
                        }
                    } catch (error) {
                        console.error(`Error uploading Order ${order.order_id}:`, error.message);
                        failedOrders.push({ order_id: order.order_id, error: error.response ? error.response.data : error.message });
                    }
                })
            );
        }

        if (failedOrders.length) {
            console.log("Failed orders:", failedOrders);
        } else {
            console.log("All orders uploaded successfully.");
        }

    } catch (err) {
        console.error("Error reading or processing file:", err.message);
    }
};

